#define CHAR_IS_UNRESERVED(c) (uri_chars[(unsigned char)(c)])

//-.0123456789ABCDEFGHIJKLMNOPQRSTUVWXYZ_abcdefghijklmnopqrstuvwxyz~
std::string url_encode(std::string_view str, bool space_as_plus)
{
    std::string result;
    result.reserve(str.size() * 1.2); // 预分配优化
    
    static const char* hex = "0123456789ABCDEF";
    for(unsigned char c : str)
    {
        if(uri_chars[c])
        {
            result += c;
        }
        else if(space_as_plus && c == ' ')
        {
            result += '+';
        }
        else
        {
            result += {'%', hex[c >> 4], hex[c & 0x0F]};
        }
    }
    return result;
}

std::string url_decode(std::string_view str, bool space_as_plus)
{
    std::string result;
    result.reserve(str.size());
    
    for(auto it = str.begin(); it != str.end(); ++it)
    {
        if(*it == '+' && space_as_plus)
        {
            result += ' ';
        }
        else if(*it == '%' && std::distance(it, str.end()) >= 3)
        {
            const unsigned char c1 = xdigit_chars[static_cast<unsigned char>(*(it+1))];
            const unsigned char c2 = xdigit_chars[static_cast<unsigned char>(*(it+2))];
            
            if(c1 != 0 || *(it+1) == '0') // 有效十六进制
            {  
                result += static_cast<char>((c1 << 4) | c2);
                it += 2;
            }
            else // 无效编码，保留原样
            {
                result.append(it, it+3);
                it += 2;
            }
        }
        else
        {
            result += *it;
        }
    }
    return result;
}

